/**
 * Mock Backend Server for Saarthi.AI
 * Simulates AWS Lambda + API Gateway responses for local testing
 */

const express = require('express');
const cors = require('cors');
const multer = require('multer');
const upload = multer({ storage: multer.memoryStorage() });

const app = express();
app.use(cors());
app.use(express.json({ limit: '50mb' }));

const PORT = 3001;

console.log('🚀 Starting Mock Backend Server...\n');

// Health check
app.get('/health', (req, res) => {
  res.json({ success: true, data: { status: 'ok' } });
});

// RAG Query endpoint
app.post('/query', (req, res) => {
  const { query, language } = req.body;
  console.log(`📝 Query: "${query}" (${language})`);
  
  setTimeout(() => {
    res.json({
      success: true,
      data: {
        answer: `Mock answer for: "${query}" in ${language}. This is a simulated response for testing purposes. The actual response would come from Claude 3 Sonnet via Bedrock.`,
        sources: [
          { title: 'Mock Government Scheme Document', url: 'https://example.com/scheme1' },
          { title: 'Mock Policy PDF', url: 'https://example.com/policy1' }
        ],
        confidence: 0.85
      }
    });
  }, 1000);
});

