// Voice STT endpoint
app.post('/voice/stt', upload.single('audio'), (req, res) => {
  console.log(`🎤 STT Request: ${req.file?.originalname || 'audio file'}`);
  
  setTimeout(() => {
    res.json({
      success: true,
      data: {
        text: 'This is a mock transcription of the audio file. The actual speech-to-text conversion would happen using Amazon Transcribe.'
      }
    });
  }, 2000);
});

// Voice TTS endpoint
app.post('/voice/tts', (req, res) => {
  const { text, language } = req.body;
  console.log(`🔊 TTS Request: "${text.substring(0, 50)}..." (${language})`);
  
  setTimeout(() => {
    // Return mock base64 audio (silent audio WAV file)
    const mockAudioBase64 = 'UklGRiQAAABXQVZFZm10IBAAAAABAAEAQB8AAEAfAAABAAgAZGF0YQAAAAA=';
    res.json({
      success: true,
      data: {
        audio: mockAudioBase64
      }
    });
  }, 1500);
});

